    //method to connect database
    private ResultSet doConnect() {
        resultSet = null;
        String sql = "SELECT * FROM MARTIN.ACCOUNTS WHERE ACCOUNT_NUMBER=? AND PIN=?";
        try {
            con = DriverManager.getConnection("jdbc:derby://localhost:1527/Accounts", "martin", "admin");
            statement = con.prepareStatement(sql);
            statement.setString(1, this.accountNumber);
            statement.setString(2, this.pin);
            resultSet = statement.executeQuery();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
        return resultSet;
    }
    //method to check credential during log in into ATM
    private void checkCredentials(ResultSet set) {

        try {
            if (set.next()) {
                mainText.setText("Login succesful\n Choose your option!");
                buttonDeposit.setEnabled(true);
                buttonExit.setEnabled(true);
                buttonWithdraw.setEnabled(true);
                buttonBalance.setEnabled(true);
                lableBalance.setVisible(true);
                labelWithdraw.setVisible(true);
                labelDeposit.setVisible(true);
                labelExit.setVisible(true);

                balance = resultSet.getInt("BALANCE");

            } else {
                mainText.setText("Wrong login credential, login unsuccesful, try again\nPlease enter your account number:\n"
                );
                this.accountNumber = "";
                this.pin = "";
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }
    //updateBalance method- updating balance in database
    private void updateBalance(Integer newBalance) {
        try {
            if (resultSet.next()) {

                resultSet.updateInt("BALANCE", newBalance);
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }

    }
