    private void jButtonEnterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEnterActionPerformed
        if (mainText.getText().contains("Enter the deposit amount")) {
            Integer newBalance = balance + (Integer.parseInt(this.entry));
            updateBalance(newBalance);
            mainText.setText("The deposit of " + this.entry + " dollars, has been completed!");
        } else if (mainText.getText().contains("Enter the amount of cash")) {
            Integer newBalance = balance - (Integer.parseInt(this.entry));
            updateBalance(newBalance);
            mainText.setText("Operation succesful, please take your money!");
        } else if (mainText.getText().contains("Please hit the Enter button if you wanna log out")) {
            reset();
        } else if (this.entry.isEmpty()) {
            this.entry = "";
        } else {
            if (this.accountNumber.isEmpty()) {
                this.accountNumber = this.entry;
                mainText.setText("Enter your PIN number:\n");
            } else {
                this.pin = this.entry;
                resultSet = doConnect();
                checkCredentials(resultSet);
            }
        }

        this.entry = "";
        System.out.println("account:" + this.accountNumber);
        System.out.println("pin" + this.pin);
    }//GEN-LAST:event_jButtonEnterActionPerformed

